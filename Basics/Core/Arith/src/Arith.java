public class Arith {
    public static void main(String[] args){
        int num1=5; // if number is there + act as add in java
        int n1=54;
        System.out.println(n1);
        System.out.println(num1++);
        System.out.println(num1);
        System.out.println(++num1);
        System.out.println(num1);
    }
}
