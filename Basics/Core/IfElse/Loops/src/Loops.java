public class Loops {
    public static void main(String[] args){
        int num1=0;
        while(num1<=10){
            System.out.println(num1);
            num1++;
        }
        do{
            System.out.println(num1);
        }while(num1<=10);
        for(int i=1;i<=10;i++){
            System.out.println(i + "hello world");
        }
    }
}
