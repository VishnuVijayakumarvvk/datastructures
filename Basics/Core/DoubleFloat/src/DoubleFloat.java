public class DoubleFloat {
    public static void main(String[] args){
        int sum=22/7;
        float sum1=22/7f;
        double sum2=22/7d;
        char alpha='D';
        boolean isDone=true;
        char mySymbol='D';
        String unicode=Integer.toHexString(mySymbol);
        char toChar='\u0099';
        System.out.println(sum);
        System.out.println(sum1);
        System.out.println(sum2);
        System.out.println(alpha);
        System.out.println(isDone);
        System.out.println(unicode);
        System.out.println(toChar);
        String firstName="vishnu";
        String secondName="vijayakumar";
        System.out.println(firstName+" "+secondName);
        System.out.println(firstName.toUpperCase());
        String myName="My name is Vishnu";
        System.out.println(myName.trim());
        String proof=" I am a football player ";
        System.out.println(proof.trim());
    }
}
